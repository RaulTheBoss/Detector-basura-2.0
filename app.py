class Detector(VideoProcessorBase):

    def recv(self, frame):

        global latitud, longitud

        imagen = frame.to_ndarray(format="bgr24")

        # Si la inspección no ha iniciado, solo mostrar la cámara
        if not st.session_state.inspeccion_iniciada:
            return av.VideoFrame.from_ndarray(
                imagen,
                format="bgr24"
            )

        resultados = modelo.track(
            imagen,
            persist=True,
            tracker="bytetrack.yaml",
            conf=confianza,
            verbose=False
        )

        resultado = resultados[0]

        # ==================================================
        # CONTAR SOLO IDS NUEVOS
        # ==================================================

        if (
            resultado.boxes is not None
            and resultado.boxes.id is not None
        ):

            ids = resultado.boxes.id.cpu().numpy().astype(int)
            clases = resultado.boxes.cls.cpu().numpy().astype(int)
            confs = resultado.boxes.conf.cpu().numpy()

            for obj_id, clase, conf in zip(ids, clases, confs):

                # Si ya fue contado anteriormente
                if obj_id in st.session_state.ids_contados:
                    continue

                st.session_state.ids_contados.add(obj_id)

                nombre = modelo.names[clase]

                st.session_state.conteo[nombre] += 1

                # Guardar captura
                ruta_imagen = guardar_captura(imagen)

                # Registrar información
                registro = {

                    "fecha": datetime.now().strftime("%Y-%m-%d"),

                    "hora": datetime.now().strftime("%H:%M:%S"),

                    "id_objeto": int(obj_id),

                    "clase": nombre,

                    "confianza": round(float(conf), 3),

                    "latitud": latitud,

                    "longitud": longitud,

                    "imagen": ruta_imagen

                }

                st.session_state.registro.append(
                    registro
                )

        # ==================================================
        # DIBUJAR DETECCIONES
        # ==================================================

        salida = resultado.plot()

        return av.VideoFrame.from_ndarray(
            salida,
            format="bgr24"
        )


