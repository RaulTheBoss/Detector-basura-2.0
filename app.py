def guardar_captura(imagen):

    nombre = datetime.now().strftime(
        "%Y%m%d_%H%M%S_%f"
    )

    ruta = os.path.join(
        CAPTURAS_DIR,
        f"{nombre}.jpg"
    )

    cv2.imwrite(ruta, imagen)

    st.session_state.capturas.append(
        ruta
    )

    return ruta


def crear_zip():

    zip_path = os.path.join(
        tempfile.gettempdir(),
        "capturas.zip"
    )

    with zipfile.ZipFile(
        zip_path,
        "w"
    ) as z:

        for archivo in st.session_state.capturas:

            if os.path.exists(archivo):

                z.write(
                    archivo,
                    os.path.basename(archivo)
                )

    return zip_path

